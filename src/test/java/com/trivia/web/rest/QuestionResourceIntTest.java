    @Before
    public void setup() {
        MockitoAnnotations.initMocks(this);
        final QuestionResource questionResource = new QuestionResource(questionRepository);
        this.restQuestionMockMvc = MockMvcBuilders.standaloneSetup(questionResource)
            .setCustomArgumentResolvers(pageableArgumentResolver)
            .setControllerAdvice(exceptionTranslator)
            .setConversionService(createFormattingConversionService())
            .setMessageConverters(jacksonMessageConverter).build();
    }

    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Question createEntity(EntityManager em) {
        Question question = new Question()
            .question(DEFAULT_QUESTION)
            .answer1(DEFAULT_ANSWER_1)
            .answer2(DEFAULT_ANSWER_2)
            .answer3(DEFAULT_ANSWER_3)
            .answer4(DEFAULT_ANSWER_4)
            .correctAnswer(DEFAULT_CORRECT_ANSWER)
            .time(DEFAULT_TIME);
        return question;
    }

    @Before
    public void initTest() {
        question = createEntity(em);
    }

    @Test
    @Transactional
    public void createQuestion() throws Exception {
        int databaseSizeBeforeCreate = questionRepository.findAll().size();

        // Create the Question
        restQuestionMockMvc.perform(post("/api/questions")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(question)))
            .andExpect(status().isCreated());

        // Validate the Question in the database
        List<Question> questionList = questionRepository.findAll();
        assertThat(questionList).hasSize(databaseSizeBeforeCreate + 1);
        Question testQuestion = questionList.get(questionList.size() - 1);
        assertThat(testQuestion.getQuestion()).isEqualTo(DEFAULT_QUESTION);
        assertThat(testQuestion.getAnswer1()).isEqualTo(DEFAULT_ANSWER_1);
        assertThat(testQuestion.getAnswer2()).isEqualTo(DEFAULT_ANSWER_2);
        assertThat(testQuestion.getAnswer3()).isEqualTo(DEFAULT_ANSWER_3);
        assertThat(testQuestion.getAnswer4()).isEqualTo(DEFAULT_ANSWER_4);
        assertThat(testQuestion.getCorrectAnswer()).isEqualTo(DEFAULT_CORRECT_ANSWER);
        assertThat(testQuestion.getTime()).isEqualTo(DEFAULT_TIME);
    }

